    private void button_plusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_plusActionPerformed
        if (text_idberas.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Silahkan lengkapi data barang dahulu");
        } else {
            int jml = Integer.parseInt(text_jumlahbeli.getText());
            int hasilbeli = jml + 1;
            text_jumlahbeli.setText(String.valueOf(hasilbeli));
            int hargaberas = Integer.parseInt(text_harga.getText());
            int jumlahbeli = Integer.parseInt(text_jumlahbeli.getText());
            int total = hargaberas * jumlahbeli;
            text_total.setText(String.valueOf(total));

        }

    }//GEN-LAST:event_button_plusActionPerformed

